                                <p align="justify"> Dr. Samith Buddika earned his BSc.Eng degree with First Class Honors in Civil Engineering (2009) from the University of Peradeniya in Sri Lanka. He joined the department as a probationary lecturer in 2011. He obtained his M.Eng (2013) and PhD (2017) from the Tokyo Institute of Technology (TIT), Japan. He is a member of Earthquake reconnaissance survey team from TIT to Nepal after 2015 Nepal earthquake that killed nearly 9,000 people and injured nearly 22,000.<br><br>

Dr. Samith Buddika's research is in the area of Earthquake-resistant design and analysis of structures, Vibration-based Health Monitoring, UAV based health Monitoring, intelligent structural damage identification using machine learning techniques and sustainable concrete materials.<br><br>

He has won the Access Engineering PLC Award for the “Best Paper” published in Society of Structural Engineers Sri Lanka (SSE SL) in 2021, Award for the “Best Paper” in Annual Sessions of SSE SL in 2022, and The President's Awards for Scientific Research from the Government of Sri Lanka in 2018.<br><br>

Dr. Samith Buddika worked as the consulting structural expert for several major constructions and consulting companies in Sri Lanka.</p>   
                 
