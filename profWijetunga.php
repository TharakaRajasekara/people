                            <li>Prof. J. J. Wijetunge received his Ph.D. in Coastal Engineering from Cambridge University, UK in 1997 and joined the Department of Civil Engineering, University of Peradeniya, Sri Lanka in the same year. He has also been a Visiting Professor at University of Oxford, UK, Visiting Research Professor at University of Southern California, USA and Visiting Scientist at Cornell University, USA during his sabbatical leave. He served as the Director of the Centre for Engineering Research and Postgraduate Studies (CERPS) as well as the Chairman of the Research Committee of the Faculty of Engineering from 2011 to 2017. His research areas include wave-structure interaction, tsunami and storm surge hydrodynamics, coastal hazard assessments, and wave-current boundary layers. Prof. Wijetunge was the recipient of the prestigious CVCD Excellence Award in 2014 as the Most Outstanding Senior Researcher in Technology, Engineering and related Sciences in the Sri Lankan universities. He also received the Presidential award for scientific research publications in 2009, 2012 and 2013 as well as the National Science Foundation Research Award for scientific excellence in the field of Engineering Sciences twice in 2008 and in 2017. Prof. Wijetunge has published over 80 papers in national and international journals and conference proceedings and his paper on the effects of sediment transport on bed friction and turbulence received the American Society of Civil Engineers Award for the Best Paper in the Journal of Waterway, Port, Coastal and Ocean Engineering. He serves on several national level committees including the Research Advisory Board of the National Science Foundation, Steering Committee on Coastal Hazards Early Warning at the Ministry of Disaster management, Standing Committee on Climate Change and Natural Disasters at the National Science Foundation, and Standing Committee on Postgraduate Studies and Research at the University Grants Commission. He was also one of the founding coordinators of the postgraduate degree programme in disaster management at the Peradeniya University. Prof. Wijetunge has also been an invited speaker in several countries; has chaired at the international conferences and has been on the international scientific committees. He was the editor of two international conferences and also serves on the review panels of several national and international journals.</li>
