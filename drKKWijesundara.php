                        <ul>
                            <li>Dr. Kushan Kalmith Wijesundara received the Bachelor Degree in Science specialized in Civil Engineering from the University of Peradeniya, Sri Lanka in 2003. He was awarded a scholarship by the University of Pavia, Italy to pursue M.Sc. and Ph.D. at the ROSE School attached to the University of Pavia, Italy. He worked on shear strength of precast pre-stressed concrete hollow core slabs for his Master dissertation. He received his M.Sc. specialized in Earthquake Engineering in May 2005. In the same year, he enrolled in the Ph.D. Program in Earthquake Engineering at the same institute. He works on the Direct Displacement Based Seismic Design of Steel Concentrically Braced Frame with RHS Shape Brace and Finite Element Formulation of Brace Element for Post Buckling Behaviour. He received his Ph.D. in December 2009. He served as a researcher at the European Centre for Training and Research in Earthquake Engineering (EUCENTRE) in Pavia, Italy from December 2009 to May 2011 and at BRGM (French Geological Survey), Natural Risks Division in Orleans, France from August, 2011 to March, 2012. From 2012 to January 2015 he served as a senior lecturer at South Asian Institution of Technology and Medicine, Malabe. Currently, he is serving as a senior lecturer at the department of civil engineering, faculty of engineering, University of Peradeniya.
                                He has ten international journal publications and twenty five international conference publications. His research interest is on finite element formulation and modelling of structures for extreme loadings, structural dynamics, development of direct displacement based design philosophy and estimation of modal properties of structures using ambient vibration measurements through wavelet transformation. He is a reviewer of Journal of Engineering Structures and Journal Civil Engineering and Environmental Systems.
                                Furthermore, he has been responsible for coordination of many local and international projects in the area of nonlinear finite element modelling of structures and analysis for dynamic loading in last few years.</li>
