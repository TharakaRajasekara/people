          <div align="justify">Dr. Mohamed Nasvi is currently working as Senior Lecturer at Department of Civil Engineering, University of Peradeniya (UoP), Sri Lanka. He completed his 
              BSc (Hons) Civil Engineering degree (First Class Honours with three awards) from UoP, Sri Lanka in 2009 and PhD degree in Geotechnical Engineering from Monash University, 
              Australia in 2014. After the PhD, he worked as casual research officer at Monash University from October 2013 to March 2014. After joining UoP in 2015, he developed his 
