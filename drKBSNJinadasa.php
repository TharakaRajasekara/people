                        <a style="font-weight: bold">Tsunami Mitigation by Coastal Vegetation (2004-2010)</a><br>
                        <ul>
                           <div align="justify">Coastal vegetation acts as a natural barrier against extreme natural and anthropogenic activities, protecting infrastructure 
                               and human lives. Establishment of hard infrastructure for tsunami protection is not feasible in developing countries due to its cost-intensive 
                               nature. Coastal vegetation can therefore be a feasible alternative for tsunami and general coastal protection in developing countries. 
                               This project investigated the effectiveness of current coastal vegetation projects and reports a pilot-scale vegetation project, which provides 
                               insights into the management and sustainability of such projects. Pilot project was implemented in the southern region of Sri Lanka in 
                               Collaboration with Local Administrative Office. The project revealed the importance of adopting scientific and non-scientific methods of 
                               vegetation establishment and selecting appropriate species and structure of vegetation strips for tsunami protection. Community engagement 
                               was crucial to success of these projects. <br>

                        </ul>
