        <div class="panel-body clearfix panel-border"> 
          <div align="justify">Prof. Hiran Yapa is a Professor at the Civil 
            Engineering Department of the University of Peradeniya. He obtained 
            his bachelor's degree from the University of Moratuwa in 2006 and 
            thereafter worked as a research assistant at the same university till 
            he went to the Cambridge University in 2007 to read his PhD. His PhD 
            thesis was 'Optimum shear strengthening of reinforced concrete beams' 
            and it included experiments on shear retrofitting, strengthened behaviour 
            prediction, and use of stochastic optimisation techniques for structural 
            optimisation. Upon completion, he joined in the University of Peradeniya 
            in 2011. Since then, he teaches structural design, prestressed concrete, 
            concrete technology, etc. in the bachelor's and postgraduate degree 
            programmes. He also engages actively in research and consultancy projects. 
