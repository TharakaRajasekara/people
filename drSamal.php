          <p align="justify">Dr. Samal Dharmarathna is currently working as a Senior Lecturer (Grade II) at the Department of Civil Engineering, Faculty of Engineering, University 
              of Peradeniya, Sri Lanka. He received his BScEng (Hons) degree from the Department of Civil Engineering, University of Peradeniya in 2007. Then he joined industry 
              and worked one year each at CML - MTD Construction Ltd and Mahaweli Authority of Sri Lanka as a Project Engineer and Civil Engineer respectively. He earned his Master 
              of Philosophy (MPhil) degree from the Department of Civil Engineering, University of Peradeniya in 2013. Upon completion, he worked as a Lecturer at International 
              Training Institute of Irrigation & Water Management, Kothmale for a period of three years. In 2015 he entered the Department of Civil Engineering, the University of 
              Tokyo, Japan and completed his Doctor of Philosophy (Ph.D.) degree in 2018. His Ph.D. thesis title was "Comparative Analysis of Sequential Time Discount Rate in 
              Differential Disastrous Networks". After completion, he worked as a postdoctoral researcher at Transportation Research and Infrastructure Planning (TRIP) laboratory 
              at the Department of Civil engineering, the University of Tokyo, Japan for a period of six months and returned Sri Lanka in April 2019.</p>
                        
